

public class Main{
    public static void main(String[] args) {
        int n = 10;
        if(n % 2 == 0) {
            System.out.println("n is even");
        } else {
            System.out.println("n is odd");
        }

        // This is a simple Java program that checks if a number is even or odd.
        // The program uses the modulus operator to determine if the number is divisible by 2.
        // If the number is divisible by 2, it prints "n is even"; otherwise, it prints "n is odd".
    }
}